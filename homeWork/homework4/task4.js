
//4.1 - Обчислення площі кола
let radius = 5;
let circleArea = Math.PI * Math.pow(radius, 2);

console.log("Площа кола =", circleArea.toFixed(2));


//4.2 - Обчислення площі прямокутника
let length = 4.2;
let width = 5.4;
let rectangleArea = length * width;

console.log("Проща прямокутника =", rectangleArea.toFixed(2));


//4.3 - Обчислення площі циліндра
let radiusСylinder = 5.5;
let heightCylinder = 8.2;
let cylinderArea = Math.PI * Math.pow(radiusСylinder, 2) * heightCylinder;

console.log("Площа циліндлра =", cylinderArea.toFixed(2));